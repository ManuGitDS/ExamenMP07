<?php


class clsCatalog
{
    private $xml;
    private $xmlfile = 'xmldb/catalog.xml';

    function __construct()
    {
        $this->LoadCatalog();
    }
    ///////////////////////////////////////////////////////////////////////////
    function LoadCatalog()
    {
        if (file_exists($this->xmlfile)) {
            $this->xml = simplexml_load_file($this->xmlfile);
        } else {

            $this->xml = "Catalogo vacÃ­o o no existe";
        }
    }
    //////////////////////////////////////////////////////////////////////////////////
    function show()
    {
        //header('Content-Type: application/xml; charset=utf-8');
        echo $this->xml->asXML();
    }
    ///////////////////////////////////////////////////////////////////////////


    function itemExist($id_product)
    {
        foreach ($this->xml->product_item as $item) {
            $itemId = $item->id_product;
            if ($itemId == $id_product) {
                return true;
            }
        }

        return false;
        
    }
    ///////////////////////////////////////////////////////////////////////////
    ///////////////////////////////////////////////////////////////////////////
